package Model;

/**
 *
 * @author ashmitbakshi
 */
public class User {
    
    
    private String name;
    private String pwd;

    public String getName() {
        return name;
    }

    public String getPwd() {
        return pwd;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setPwd(String pwd) {
        this.pwd = pwd;
    }
    
    public String toString(){
        
        return name+" "+pwd;
    }
    
   
    
}
